import { useState, useRef, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { FileDown, X, FileJson, AlertCircle } from 'lucide-react';
import { Link } from 'react-router-dom';
import { useLanguage } from '../contexts/language-context';
import { useVideos, Video } from '../contexts/video-context';

export default function ImportModal() {
    const navigate = useNavigate();
    const { language } = useLanguage();
    const { addVideo, videos } = useVideos();
    const isEnglish = language === 'en';

    // State
    const [file, setFile] = useState<File | null>(null);
    const [previewData, setPreviewData] = useState<Video[] | null>(null);
    const [error, setError] = useState<string | null>(null);
    const [mounted, setMounted] = useState(false);
    const fileInputRef = useRef<HTMLInputElement>(null);

    useEffect(() => {
        setMounted(true);
    }, []);

    // Translations
    const t = {
        title: isEnglish ? 'Import Backup' : 'Importar Backup',
        dropLabel: isEnglish ? 'Drop JSON file here or click to upload' : 'Arrastra el archivo JSON aquí o click para subir',
        importBtn: isEnglish ? 'Import' : 'Importar',
        cancelBtn: isEnglish ? 'Cancel' : 'Cancelar',
        invalidFile: isEnglish ? 'Invalid JSON file' : 'Archivo JSON inválido',
        noData: isEnglish ? 'No valid videos found in file' : 'No se encontraron videos válidos',
        videosFound: isEnglish ? 'Videos found:' : 'Videos encontrados:',
        success: isEnglish ? 'Import successful!' : '¡Importación exitosa!',
    };

    const handleFileChange = (e: React.ChangeEvent<HTMLInputElement>) => {
        const f = e.target.files?.[0];
        if (f) processFile(f);
    };

    const processFile = (f: File) => {
        setFile(f);
        setError(null);
        setPreviewData(null);

        const reader = new FileReader();
        reader.onload = (e) => {
            try {
                const text = e.target?.result as string;
                const json = JSON.parse(text);

                // Validate if it is an array of videos
                if (Array.isArray(json) && json.length > 0 && json[0].url) {
                    setPreviewData(json);
                } else if (json.videos && Array.isArray(json.videos)) {
                    // Handle case where it might be wrapped in { videos: [...] }
                    setPreviewData(json.videos);
                } else {
                    setError(t.noData);
                }
            } catch (err) {
                setError(t.invalidFile);
            }
        };
        reader.readAsText(f);
    };

    const handleImport = () => {
        if (!previewData) return;

        const existingUrls = new Set(videos.map(v => v.url));

        previewData.forEach(video => {
            if (!existingUrls.has(video.url)) {
                addVideo({
                    name: video.name,
                    url: video.url,
                    tags: video.tags
                });
            }
        });

        // Navigate back
        navigate('/');
    };

    if (!mounted) return null;

    return (
        <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50 backdrop-blur-sm p-4">
            <div className="bg-white dark:bg-zinc-900 w-full max-w-md rounded-xl shadow-2xl border border-zinc-200 dark:border-zinc-800 flex flex-col overflow-hidden animate-in fade-in zoom-in-95 duration-200">

                {/* Header */}
                <div className="flex items-center justify-between p-4 border-b border-zinc-100 dark:border-zinc-800">
                    <h1 className="text-lg font-semibold flex items-center gap-2 text-zinc-900 dark:text-zinc-100">
                        <FileDown className="w-5 h-5" />
                        {t.title}
                    </h1>
                    <Link to="/" className="p-1 rounded-full hover:bg-zinc-100 dark:hover:bg-zinc-800 transition-colors cursor-pointer">
                        <X className="w-5 h-5 text-zinc-500" />
                    </Link>
                </div>

                {/* Body */}
                <div className="p-6 space-y-6">

                    {/* File Upload */}
                    <div
                        onClick={() => fileInputRef.current?.click()}
                        className={`border-2 border-dashed rounded-xl p-8 flex flex-col items-center justify-center text-center cursor-pointer transition-colors ${file ? 'border-blue-500 bg-blue-50 dark:bg-blue-900/20' : 'border-zinc-300 dark:border-zinc-700 hover:border-zinc-400 dark:hover:border-zinc-600'
                            }`}
                    >
                        <input
                            ref={fileInputRef}
                            type="file"
                            accept=".json"
                            className="hidden"
                            onChange={handleFileChange}
                        />
                        {file ? (
                            <>
                                <FileJson className="w-10 h-10 text-blue-500 mb-2" />
                                <span className="font-medium text-sm text-zinc-900 dark:text-zinc-100">{file.name}</span>
                                <span className="text-xs text-zinc-500">{(file.size / 1024).toFixed(1)} KB</span>
                            </>
                        ) : (
                            <>
                                <FileDown className="w-10 h-10 text-zinc-400 mb-2" />
                                <span className="text-sm text-zinc-500 max-w-[200px]">{t.dropLabel}</span>
                            </>
                        )}
                    </div>

                    {error && (
                        <div className="flex items-center gap-2 text-red-500 text-sm bg-red-50 dark:bg-red-900/20 p-3 rounded-lg">
                            <AlertCircle className="w-4 h-4" />
                            {error}
                        </div>
                    )}

                    {/* Preview Info */}
                    {previewData && !error && (
                        <div className="bg-zinc-50 dark:bg-zinc-800/50 rounded-lg p-4 animate-in fade-in slide-in-from-top-4 duration-300">
                            <p className="text-sm text-zinc-700 dark:text-zinc-300 font-medium">
                                {t.videosFound} <span className="text-blue-600 dark:text-blue-400">{previewData.length}</span>
                            </p>
                            <div className="mt-2 flex flex-wrap gap-1 max-h-20 overflow-y-auto">
                                {Array.from(new Set(previewData.flatMap(v => v.tags))).slice(0, 10).map(tag => (
                                    <span key={tag} className="text-xs bg-white dark:bg-zinc-800 border border-zinc-200 dark:border-zinc-700 px-2 py-1 rounded-full text-zinc-600 dark:text-zinc-400">
                                        {tag}
                                    </span>
                                ))}
                                {new Set(previewData.flatMap(v => v.tags)).size > 10 && (
                                    <span className="text-xs text-zinc-500 flex items-center">...</span>
                                )}
                            </div>
                        </div>
                    )}
                </div>

                {/* Footer */}
                <div className="p-4 border-t border-zinc-100 dark:border-zinc-800 flex justify-end gap-3 bg-zinc-50/50 dark:bg-zinc-900/50">
                    <Link
                        to="/"
                        className="px-4 py-2 text-sm font-medium text-zinc-600 dark:text-zinc-400 hover:bg-zinc-100 dark:hover:bg-zinc-800 rounded-lg transition-colors cursor-pointer"
                    >
                        {t.cancelBtn}
                    </Link>
                    <button
                        onClick={handleImport}
                        disabled={!file || !previewData}
                        className="px-4 py-2 text-sm font-medium text-white bg-blue-600 hover:bg-blue-700 disabled:opacity-50 disabled:cursor-not-allowed rounded-lg shadow-sm transition-colors flex items-center gap-2 cursor-pointer disabled:cursor-not-allowed"
                    >
                        <FileDown className="w-4 h-4" />
                        {t.importBtn}
                    </button>
                </div>
            </div>
        </div>
    );
}
